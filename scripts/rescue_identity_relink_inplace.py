#!/usr/bin/env python3
"""Offline identity-relink rescue for preprocessed ``_both.h5`` or combined
STAC-output ``_both.h5`` files.

Two situations this script handles:

1. *Preprocessed* ``preprocessed_bout_*_both.h5`` (batch_split_valid_bouts
   output, upstream of STAC IK). Use this when you fix a bug in
   ``utils/identity_relink.py`` and want to patch existing preprocessed
   files without re-running the batch_split_valid_bouts step. STAC still
   has to be re-run on the patched files afterwards, but that's much
   cheaper than the full preprocessing + STAC + combine pipeline.

2. *Combined* ``ik_output_combined_*_both.h5`` (post-STAC). Use this for
   historical files where preprocessing ran a buggy relink and you want
   the notebook's render cell to see corrected keypoints without a full
   re-run. Note that ``qpos`` / ``kp_data`` in a combined file came from
   STAC fitting on the *buggy* swap — this script row-swaps them along
   with the raw keypoints, which keeps them consistent with the corrected
   identity labels but does NOT re-run the body-model fit. If the old
   swap was bad enough that STAC mis-fit body dimensions, those arrays
   are best regenerated by re-running STAC on the rescued preprocessed
   file rather than relying on the combined-file rescue.

Algorithm per fly0/fly1 pair of bouts (consecutive in the file, by the
convention written by batch_split_valid_bouts.py):

  1. **Undo the saved swap_state.** ``joint_relink_shared_bouts`` at
     ``scripts/batch_split_valid_bouts.py:202`` applied the OLD (possibly
     buggy) swap_state to *every* per-frame array on the bout dict,
     including ``orig_keypoints``. Row-swapping every per-frame array a
     second time with that same mask is the exact inverse, restoring the
     pristine raw input. Skipping this step would feed buggy-corrected
     data back into relink and let the algorithm convince itself that
     the current state is clean (a collapsed body-length glitch hiding in
     the *other* fly's slot gets masked out by the new outlier rule and
     emits no swap signal — so nothing changes).
  2. **Run relink_pair** on the pristine ``orig_keypoints`` (or ``keypoints``
     if orig_keypoints is missing). By default this uses the live
     ``RelinkConfig()`` so any improvements to the bl-Viterbi / outlier /
     min-run logic in ``utils/identity_relink.py`` are picked up
     automatically without touching this script.
  3. **Row-swap every per-frame array** with the new swap_state so qpos,
     xpos, edge_nan, valid_fly*, post-Procrustes ``keypoints``, etc. are
     all consistent with the corrected identity labels.
  4. **Recompute ``valid_fly0`` / ``valid_fly1`` / ``valid_both``** via
     ``compute_pair_validity`` on the corrected arrays.
  5. **Write the rescued data.** By default to a new H5 path (the original
     is preserved). Pass ``--overwrite`` to write to a sibling ``.tmp``
     file first and then atomically rename over the input.

Usage:
    # Patch one file to a sibling _relinked.h5 (safe default).
    python scripts/rescue_identity_relink_inplace.py \\
        --in  /path/to/preprocessed_bout_v1_courtship_both.h5 \\
        --out /path/to/preprocessed_bout_v1_courtship_both_relinked.h5

    # Patch in place (writes to .tmp then atomic rename).
    python scripts/rescue_identity_relink_inplace.py \\
        --in /path/to/preprocessed_bout_v1_courtship_both.h5 \\
        --overwrite

    # Patch every Predictions_3D_*/preprocessing/preprocessed_bout_*_both.h5
    # under a dataset dir in place — shell loop:
    for f in /data2/.../Predictions_3D_*/preprocessing/preprocessed_bout_*_both.h5; do
        python scripts/rescue_identity_relink_inplace.py --in "$f" --overwrite
    done
"""

