# ── Bout helpers ───────────────────────────────────────────────────────────────

def build_all_bouts(cache_entry):
    """Merge valid + rejected into a single flat list sorted by start frame."""
    all_bouts = []
    for b in cache_entry.get("valid_bouts", []):
        all_bouts.append({**b, "originally_valid": True})
    for b in cache_entry.get("rejected_bouts", []):
        all_bouts.append({**b, "originally_valid": False})
    all_bouts.sort(key=lambda x: x["start"])
    for i, b in enumerate(all_bouts):
        b["unified_idx"] = i
    return all_bouts


def get_effective_bout(bout, sess_corrections):
    """Return the effective (possibly corrected) boundaries and status for a bout."""
    src  = "valid" if bout["originally_valid"] else "rejected"
    bidx = bout["bout_idx"]
    for corr in sess_corrections:
        if corr.get("source") == src and corr.get("bout_idx") == bidx:
            return {
                "start":        corr["start"],
                "end":          corr["end"],
                "manual_valid": corr.get("manual_valid", bout["originally_valid"]),
                "notes":        corr.get("notes", ""),
                "is_corrected": True,
            }
    return {
        "start":        bout["start"],
        "end":          bout["end"],
        "manual_valid": bout["originally_valid"],
        "notes":        "",
        "is_corrected": False,
    }


def apply_correction(corrections, sess_key, source, bout_idx,
                     start, end, manual_valid, notes):
    """Upsert a correction for a specific bout."""
    sess_list = corrections.setdefault(sess_key, [])
    for corr in sess_list:
        if corr.get("source") == source and corr.get("bout_idx") == bout_idx:
            corr.update({"start": start, "end": end,
                         "manual_valid": manual_valid, "notes": notes})
            return
    sess_list.append({
        "source":       source,
        "bout_idx":     bout_idx,
        "start":        start,
        "end":          end,
        "manual_valid": manual_valid,
        "notes":        notes,
    })


# ── Plotly figure builders ─────────────────────────────────────────────────────

def make_timeline_fig(all_bouts, sess_corrections, selected_idx=None):
    """Gantt-style horizontal bar chart: one bar per bout."""
    fig = go.Figure()

    for bout in all_bouts:
        eff = get_effective_bout(bout, sess_corrections)
        uid = bout["unified_idx"]
        is_selected = uid == selected_idx

        if eff["is_corrected"]:
            color = COLOR_MODIFIED
        elif eff["manual_valid"]:
            color = COLOR_VALID
        else:
            color = COLOR_REJECTED

        reason = bout.get("rejection_reason", "")
        hover = (
            f"Bout {uid}<br>"
            f"Start: {eff['start']}<br>"
            f"End: {eff['end']}<br>"
            f"Duration: {eff['end'] - eff['start']} frames<br>"
            f"{'Valid' if eff['manual_valid'] else ('Rejected: ' + reason)}"
            f"{'<br><i>MODIFIED</i>' if eff['is_corrected'] else ''}"
        )

        fig.add_trace(go.Bar(
            x=[eff["end"] - eff["start"]],
            y=[uid],
            base=[eff["start"]],
            orientation="h",
            marker_color=color,
            marker_line_color="#222" if is_selected else color,
            marker_line_width=3 if is_selected else 0,
            opacity=1.0 if is_selected else 0.75,
            hovertext=hover,
            hoverinfo="text",
            showlegend=False,
        ))

    # Invisible scatter markers — one per bout — so on_select fires via pointIndex.
    # Bar traces don't register as selectable points in Streamlit's on_select.
    _mids, _yvals = [], []
    for bout in all_bouts:
        eff_b = get_effective_bout(bout, sess_corrections)
        _mids.append((eff_b["start"] + eff_b["end"]) / 2)
        _yvals.append(bout["unified_idx"])
    fig.add_trace(go.Scatter(
        x=_mids, y=_yvals,
        mode="markers",
        marker=dict(size=24, opacity=0.01, color="rgba(0,0,0,0)"),
        hoverinfo="skip",
        showlegend=False,
    ))

    n = len(all_bouts)
    fig.update_layout(
        height=max(220, min(30 * n + 80, 600)),
        margin=dict(l=50, r=20, t=40, b=40),
        xaxis_title="Frame",
        yaxis_title="Bout",
        barmode="overlay",
        plot_bgcolor="#f8f8f8",
        title="Click a bout bar to inspect · Green=valid · Red=rejected · Yellow=modified",
        title_font_size=12,
    )
    fig.update_yaxes(
        tickmode="array",
        tickvals=list(range(n)),
        ticktext=[str(i) for i in range(n)],
        autorange="reversed",
    )
    return fig


CONF_THRESHOLD = 0.8  # matches CFG["conf_threshold"] in the notebook


def make_trace_fig(kp_pos, bout_start, bout_end, fps=800, view_start=None, view_end=None):
    """Three-panel figure: Y leg tips (top), Z leg tips (middle), mean confidence (bottom).

    kp_pos: dict {kp_name: np.array(n_frames, 4)} with columns [x, y, z, conf].
    view_start/view_end: window of frames to display (defaults to bout ± CONTEXT_FRAMES).
    """
    n_frames = max(len(arr) for arr in kp_pos.values()) if kp_pos else 1
    if view_start is None:
        view_start = max(0, bout_start - CONTEXT_FRAMES)
    if view_end is None:
        view_end = min(n_frames - 1, bout_end + CONTEXT_FRAMES)
    plot_start, plot_end = int(view_start), int(view_end)
    frames = np.arange(plot_start, plot_end + 1)

    fig = make_subplots(
        rows=3, cols=1,
        shared_xaxes=True,
        subplot_titles=("Y — leg tip (mm)", "Z — leg tip (mm)", "Mean confidence"),
        vertical_spacing=0.08,
        row_heights=[0.38, 0.38, 0.24],
    )

    available_legs = [leg for leg in LEG_TIPS if leg in kp_pos]

    for leg in available_legs:
        arr     = kp_pos[leg]
        clipped = frames[frames < len(arr)]
        color   = LEG_COLORS.get(leg, "#888")

        fig.add_trace(go.Scatter(
            x=clipped, y=arr[clipped, 1],
            mode="lines", name=leg,
            line=dict(color=color, width=1.5),
            legendgroup=leg,
        ), row=1, col=1)

        fig.add_trace(go.Scatter(
            x=clipped, y=arr[clipped, 2],
            mode="lines", name=leg,
            line=dict(color=color, width=1.5),
            legendgroup=leg, showlegend=False,
        ), row=2, col=1)

    # Mean confidence across all keypoints
    conf_arrays = []
    for kp, arr in kp_pos.items():
        if arr.shape[1] >= 4:
            clipped_c = frames[frames < len(arr)]
            conf_arrays.append(arr[clipped_c, 3])
    if conf_arrays:
        min_len = min(len(a) for a in conf_arrays)
        clipped_frames = frames[frames < n_frames][:min_len]
        mean_conf = np.mean([a[:min_len] for a in conf_arrays], axis=0)

        fig.add_trace(go.Scatter(
            x=clipped_frames, y=mean_conf,
            mode="lines", name="mean conf",
            line=dict(color="#555", width=1.2),
            fill="tozeroy", fillcolor="rgba(100,100,100,0.08)",
            showlegend=False,
        ), row=3, col=1)

    dur_s = (bout_end - bout_start) / fps

    shapes = [
        dict(type="rect", xref="x", yref="paper",
             x0=bout_start, x1=bout_end, y0=0, y1=1,
             fillcolor="rgba(44,160,44,0.10)", layer="below",
             line=dict(width=0), editable=False),
        dict(type="line", xref="x", yref="paper",
             x0=bout_start, x1=bout_start, y0=0, y1=1,
             line=dict(color=COLOR_VALID, dash="dash", width=2),
             editable=False),
        dict(type="line", xref="x", yref="paper",
             x0=bout_end, x1=bout_end, y0=0, y1=1,
             line=dict(color=COLOR_REJECTED, dash="dash", width=2),
             editable=False),
        dict(type="line", xref="paper", yref="y3",
             x0=0, x1=1, y0=CONF_THRESHOLD, y1=CONF_THRESHOLD,
             line=dict(color="red", dash="dot", width=1),
             editable=False),
    ]

    fig.add_annotation(
        x=1, y=CONF_THRESHOLD, xref="paper", yref="y3",
        text=f"thr {CONF_THRESHOLD}", showarrow=False,
        font=dict(size=9, color="red"), xanchor="right",
    )

    fig.update_layout(
        shapes=shapes,
        height=680,
        margin=dict(l=55, r=20, t=50, b=40),
        hovermode="x",
        template="none",
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="right", x=1),
        title=dict(
            text=(f"🟢 {bout_start} → 🔴 {bout_end} "
                  f"({bout_end - bout_start} frames, {dur_s:.2f} s)"),
            font=dict(size=11),
        ),
    )
    fig.update_xaxes(title_text="Frame", row=3, col=1)
    fig.update_yaxes(title_text="Y (mm)", row=1, col=1)
    fig.update_yaxes(title_text="Z (mm)", row=2, col=1)
    fig.update_yaxes(title_text="Confidence", row=3, col=1, range=[0, 1.05])
    return fig


# ── Skeleton topology (ordered chains: body → tip) ────────────────────────────
_LEG_CHAINS = [
    ["T1R_ThxCx", "T1R_Fe", "T1R_Ti", "T1R_Ta", "T1R_TaTip"],
    ["T1L_ThxCx", "T1L_Fe", "T1L_Ti", "T1L_Ta", "T1L_TaTip"],
    ["T2R_ThxCx", "T2R_Fe", "T2R_Ti", "T2R_Ta", "T2R_TaTip"],
    ["T2L_ThxCx", "T2L_Fe", "T2L_Ti", "T2L_Ta", "T2L_TaTip"],
    ["T3R_ThxCx", "T3R_Fe", "T3R_Ti", "T3R_Ta", "T3R_TaTip"],
    ["T3L_ThxCx", "T3L_Fe", "T3L_Ti", "T3L_Ta", "T3L_TaTip"],
]
_BODY_CHAINS = [
    ["EyeL", "Antenna_Base", "EyeR"],
    ["Antenna_Base", "Scutellum"],
    ["Scutellum", "Abd_A4"],
]
# Leg-tip keypoints mapped to their leg color (same as LEG_COLORS)
_COXA_KPS = {f"T{n}{s}_ThxCx": LEG_COLORS.get(f"T{n}{s}_TaTip", "#999")
             for n in "123" for s in "LR"}


def _skeleton_proj(kp_pos, fi, available, coord_a, coord_b):
    """Build two coord lists (None-separated) for skeleton edges at frame fi.
    coord_a/b are column indices into kp_pos arrays: 0=X, 1=Y, 2=Z."""
    as_, bs_ = [], []
    for chain in _LEG_CHAINS + _BODY_CHAINS:
        for kp in chain:
            if kp in available and fi < len(kp_pos[kp]):
                as_.append(float(kp_pos[kp][fi, coord_a]))
                bs_.append(float(kp_pos[kp][fi, coord_b]))
            else:
                as_.append(None)
                bs_.append(None)
        as_.append(None)
        bs_.append(None)
    return as_, bs_


def make_animation_fig(kp_pos, bout_start, bout_end, fps=800, step=8):
    """Side-by-side animated skeleton: top-down (XY) + side view (XZ).

    kp_pos: dict {kp_name: np.array(n_frames, 4)} — same format as _build_kp_pos output.
    """
    n_frames   = max(len(arr) for arr in kp_pos.values()) if kp_pos else 1
    plot_start = max(0, bout_start - CONTEXT_FRAMES)
    plot_end   = min(n_frames - 1, bout_end + CONTEXT_FRAMES)
    indices    = list(range(plot_start, plot_end + 1, step))

    available = set(kp_pos)
    kp_list   = sorted(available)

    pt_colors = [LEG_COLORS.get(kp, _COXA_KPS.get(kp, "#aaa")) for kp in kp_list]
    pt_sizes  = [8 if kp in LEG_COLORS else 5 for kp in kp_list]

    # Fixed axis ranges from data in the animation window
    win = slice(plot_start, plot_end + 1)
    def _rng(arr, pad=0.5):
        lo, hi = float(np.nanmin(arr)), float(np.nanmax(arr))
        p = (hi - lo) * 0.05 + pad
        return [lo - p, hi + p]

    all_x = np.concatenate([kp_pos[kp][win, 0] for kp in kp_list])
    all_y = np.concatenate([kp_pos[kp][win, 1] for kp in kp_list])
    all_z = np.concatenate([kp_pos[kp][win, 2] for kp in kp_list])
    x_range = _rng(all_x)
    y_range = _rng(all_y)
    z_range = _rng(all_z)

    def frame_data(fi):
        xs = [float(kp_pos[kp][fi, 0]) if fi < len(kp_pos[kp]) else None for kp in kp_list]
        ys = [float(kp_pos[kp][fi, 1]) if fi < len(kp_pos[kp]) else None for kp in kp_list]
        zs = [float(kp_pos[kp][fi, 2]) if fi < len(kp_pos[kp]) else None for kp in kp_list]
        sk_x, sk_y = _skeleton_proj(kp_pos, fi, available, 0, 1)
        sk_x2, sk_z = _skeleton_proj(kp_pos, fi, available, 0, 2)
        in_bout = bout_start <= fi <= bout_end
        border = "rgba(44,160,44,0.9)" if in_bout else "rgba(160,160,160,0.6)"
        mk = dict(color=pt_colors, size=pt_sizes, line=dict(color=border, width=1.5))
        return [
            go.Scatter(x=sk_x, y=sk_y, mode="lines",
                       line=dict(color="#888", width=1.5), hoverinfo="skip", showlegend=False),
            go.Scatter(x=xs, y=ys, mode="markers", marker=mk,
                       text=kp_list, hoverinfo="text", showlegend=False),
            go.Scatter(x=sk_x2, y=sk_z, mode="lines",
                       line=dict(color="#888", width=1.5), hoverinfo="skip", showlegend=False),
            go.Scatter(x=xs, y=zs, mode="markers", marker=mk,
                       text=kp_list, hoverinfo="text", showlegend=False),
        ]

    # Build figure with subplots; traces must be added in the same order as frame_data
    fig = make_subplots(rows=1, cols=2,
                        subplot_titles=["Top-down (XY)", "Side view (XZ)"],
                        column_widths=[0.5, 0.5])
    init = frame_data(indices[0])
    fig.add_trace(init[0], row=1, col=1)  # XY skeleton
    fig.add_trace(init[1], row=1, col=1)  # XY dots
    fig.add_trace(init[2], row=1, col=2)  # XZ skeleton
    fig.add_trace(init[3], row=1, col=2)  # XZ dots

    frames = [go.Frame(data=frame_data(fi), name=str(fi)) for fi in indices]
    fig.frames = frames

    sliders = [{
        "steps": [
            {"args": [[f.name], {"frame": {"duration": 50, "redraw": True},
                                 "transition": {"duration": 0}}],
             "method": "animate", "label": ""}
            for f in frames
        ],
        "active": 0,
        "x": 0.0, "len": 1.0, "y": -0.02,
        "currentvalue": {"prefix": "Frame: ", "visible": True, "xanchor": "center"},
        "transition": {"duration": 0},
    }]

    fig.update_layout(
        height=540,
        margin=dict(l=50, r=20, t=60, b=90),
        plot_bgcolor="#f0f0f0",
        showlegend=False,
        updatemenus=[{
            "type": "buttons", "showactive": False,
            "x": 0.5, "y": -0.12, "xanchor": "center", "yanchor": "top",
            "buttons": [
                {"label": "▶ Play", "method": "animate",
                 "args": [None, {"frame": {"duration": 50, "redraw": True},
                                 "fromcurrent": True, "transition": {"duration": 0}}]},
                {"label": "⏸ Pause", "method": "animate",
                     "args": [[None], {"frame": {"duration": 0, "redraw": False},
                                       "mode": "immediate"}]},
                ],
            }],
        sliders=sliders,
        title=(f"Skeleton replay  ·  step={step} frames  ·  "
               f"{fps // step} Hz effective  ·  green border = in bout"),
        title_font_size=11,
    )
    fig.update_xaxes(title_text="X (mm)", range=x_range, row=1, col=1)
    fig.update_yaxes(title_text="Y (mm)", range=y_range, row=1, col=1)
    fig.update_xaxes(title_text="X (mm)", range=x_range, row=1, col=2)
    fig.update_yaxes(title_text="Z (mm)", range=z_range, row=1, col=2)
    return fig


