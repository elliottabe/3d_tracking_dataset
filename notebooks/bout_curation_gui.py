# ── Data helpers ───────────────────────────────────────────────────────────────

def load_3d_data(folder):
    csv_path = Path(folder) / "data3D.csv"
    df = pd.read_csv(csv_path, skiprows=[1], low_memory=False)
    df = df.iloc[:-1].reset_index(drop=True)
    seen, kp_names = set(), []
    for col in df.columns:
        base = col.split(".")[0]
        if base not in seen:
            seen.add(base)
            kp_names.append(base)
    return df, kp_names


def extract_xyz(df, kp_name, scale=SCALE):
    """Return (x_mm, y_mm, z_mm) arrays for a keypoint. Returns (None,None,None) if missing."""
    cols = [c for c in df.columns if c.split(".")[0] == kp_name]
    if len(cols) < 3:
        return None, None, None
    arr = df[cols[:3]].to_numpy(dtype=float) / scale
    return arr[:, 0], arr[:, 1], arr[:, 2]


def extract_confidence(df, kp_name):
    """Return the confidence array (4th column) for a keypoint, or None if missing."""
    cols = [c for c in df.columns if c.split(".")[0] == kp_name]
    if len(cols) < 4:
        return None
    return df[cols[3]].to_numpy(dtype=float)


def _build_kp_pos(df, kp_names):
    """Pre-extract all keypoint arrays from df.

    Returns {kp: np.array(n_frames, 4)} with columns [x/SCALE, y/SCALE, z/SCALE, confidence].
    Keypoints with <3 columns are excluded; those with <4 get a NaN confidence column.
    """
    kp_pos = {}
    for kp in kp_names:
        cols = [c for c in df.columns if c.split(".")[0] == kp]
        if len(cols) < 3:
            continue
        xyz  = df[cols[:3]].to_numpy(dtype=float) / SCALE
        conf = (df[cols[3]].to_numpy(dtype=float)[:, None]
                if len(cols) >= 4 else np.full((len(xyz), 1), np.nan))
        kp_pos[kp] = np.concatenate([xyz, conf], axis=1)
    return kp_pos


def load_bouts_cache(cache_path):
    p = Path(cache_path)
    if not p.exists():
        return {}
    with open(p) as f:
        return json.load(f)


