_drag_component = st.components.v1.declare_component(
    "plotly_drag", path=str(_DRAG_COMPONENT_DIR)
)


def plotly_events_ex(plot_fig, config=None, override_height=450, key=None):
    """Render a Plotly figure with draggable shapes and return relayout events.

    Uses a local custom component (plotly_drag_component/) that bundles
    Plotly.js 3.x, bypassing the old Plotly.js 1.x inside streamlit-plotly-events.
    Returns a list of [{relayout: {"shapes[N].x0": v, ...}}] when a shape is dragged.
    """
    raw = _drag_component(
        figure_json=plot_fig.to_json(),
        config_json=json.dumps(config or {}),
        height=override_height,
        key=key,
        default="[]",
    )
    if raw is None:
        return []
    try:
        return json.loads(raw) if isinstance(raw, str) else raw
    except Exception:
        return []

